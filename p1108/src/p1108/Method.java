package p1108;

public class Method {

	// 메소드 정의
	// 결과값을 리턴해서 넘겨주면 됨.
	int add(int a,int b,int c) {
		return a+b+c;
	}
	int sub(int a,int b,int c) {
		return a-b-c;
	}
	int multi(int a,int b,int c) {
		return a*b*c;
	}
	double div(int a,int b,int c) {
		return a/b/(double)c;
	}
	
	
	
	
	
	
	
}
